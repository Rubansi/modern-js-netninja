//there are several data types in javascript
//1. string - text wrapped in single or double quotes
let name = "John"; // or let name = 'John';
console.log(name);  // output: John

//2. number - numeric values (integers and floats)
let age = 30; // integer
let price = 19.99; // float
console.log(age, price); // output: 30 19.99  

//3. boolean - true or false values                         
let isStudent = true;
let isEmployed = false;
console.log(isStudent, isEmployed); // output: true false

 